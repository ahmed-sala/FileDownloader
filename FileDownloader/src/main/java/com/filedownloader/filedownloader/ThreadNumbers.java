    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int threadCount = Integer.parseInt(jTextField1.getText());
        if (threadCount <= 0) {
            JOptionPane.showMessageDialog(
                    ThreadNumbers.this,
                    "Please enter a positive number.",
                    "Invalid Input",
                    JOptionPane.ERROR_MESSAGE
            );
            return;
        }

        for (int i = 1; i <= threadCount; i++) {
            int threadNumber = i;
            Thread thread = new Thread(() -> {
                // Open a FileDownloader GUI in this thread
                SwingUtilities.invokeLater(() -> {
                    FileDownloaderGUI fileDownloader = new FileDownloaderGUI();
                    fileDownloader.setTitle("File Downloader - Thread " + threadNumber);
                    fileDownloader.setVisible(true);
                });
            });
            thread.start();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField1KeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyTyped
        // TODO add your handling code here:
        char c = evt.getKeyChar();
        if (!(Character.isDigit(c) || (c == KeyEvent.VK_BACK_SPACE) || c == KeyEvent.VK_DELETE)) {
            getToolkit().beep();
            evt.consume();
        }
    }//GEN-LAST:event_jTextField1KeyTyped

